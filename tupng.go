//生成图片的唯一id，用于分辨批量处理的各文件
func genID1() string {
	u,err := id.NewUUID(id.Version1,nil)
	if err != nil {
		glog.Error(err) //将错误信息记录为日志，可用于调试
	}
	glog.V(1).Info("the id is generated by UUID")
	return u.String()
}

